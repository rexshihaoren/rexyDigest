def test_interactive_picker_rejects_invalid_markdown_without_writing_note(tmp_path: Path) -> None:
    class BadWriter:
        model = "bad"

        def write(self, **_kwargs):
            return "# Bad\n\nNo strict KnowledgeCard structure.\n"

    corpus = _populate(tmp_path)
    public_dir = tmp_path / "Weekly_Gist" / "Public"
    public_dir.mkdir(parents=True)
    (public_dir / "Weekly_Brief_Public_2026-05-17.md").write_text("public", encoding="utf-8")
    inbox_dir = tmp_path / "KnowledgeCard_Inbox"
    answers = iter(["y", "y", "y"])

    with pytest.raises(ValueError, match="invalid deep note Markdown"):
        run_interactive_deep_note_pick(
            window=WINDOW,
            corpus_root=corpus,
            public_dir=public_dir,
            picks_root=tmp_path / "corpus" / "deep_picks",
            inbox_dir=inbox_dir,
            config=GeneratorConfig(),
            writer_factory=lambda: BadWriter(),
            input_fn=lambda _prompt: next(answers),
            output_fn=lambda _line: None,
        )

    if inbox_dir.exists():
        assert not list(inbox_dir.glob("*.md"))


def test_available_selection_ends_lists_recent_dates(tmp_path: Path) -> None:
    root = tmp_path / "corpus" / "selections"
    root.mkdir(parents=True)
    for raw in ["2026-05-17", "2026-05-10", "bad"]:
        (root / f"Selection_{raw}.jsonl").write_text("", encoding="utf-8")

    assert available_selection_ends(tmp_path / "corpus") == [
        date(2026, 5, 17),
        date(2026, 5, 10),
    ]
